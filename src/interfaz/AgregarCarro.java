    private void cmdGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdGuardarActionPerformed
       
        String placa, auxPropietario, cedula;
        int indice;
        Persona propietario;
        placa = txtPlaca.getText();
        auxPropietario = cmbPropietario.getSelectedItem().toString();
        indice = auxPropietario.indexOf("-") - 1;
        cedula = auxPropietario.substring(0, indice);
        ArrayList<Carro> carrosModificado;
         propietario = Helper.traerPersonaCedula(cedula, rutaP);
        try {
            if (aux == 0) {

               
                Carro c = new Carro(placa, propietario);
                c.guardar(salida);

            } else {
                carrosModificado = Helper.modificarCarro(rutaC, placa, propietario);
                salida = new ObjectOutputStream(new FileOutputStream(rutaC));
                Helper.volcado(salida, carrosModificado);
                aux = 0;
                Helper.mensaje(this, "Carro Actualizado Correctamente!", 1);
            }
        } catch (IOException ex) {
            Logger.getLogger(Agregar.class.getName()).log(Level.SEVERE, null, ex);
        }

        Helper.llenarTablaCarros(tblCarros, rutaC);

        txtPlaca.setText("");
        
        cmbPropietario.setSelectedIndex(0);
        txtPlaca.requestFocusInWindow();
        JButton botonesH[] = {cmdBuscar, cmdCancelar};
        JButton botonesD[] = {cmdEliminar, cmdGuardar};

        Helper.habilitarBotones(botonesH);
        Helper.deshabilitarBotones(botonesD);
    }//GEN-LAST:event_cmdGuardarActionPerformed

    private void cmdBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdBuscarActionPerformed
        String placa, auxPropietario;
        Persona propietario;
        placa = txtPlaca.getText();
        Carro c;
        if (Helper.buscarCarroPlaca(placa, rutaC)) {
            c = Helper.traerCarroPlaca(placa, rutaC);
            txtPlaca.setText(c.getPlaca());
            propietario = c.getPropietario();
            auxPropietario = propietario.getCedula() + " - " + propietario.getNombre() + " " + propietario.getApellido();
            cmbPropietario.setSelectedItem(auxPropietario);
            aux = 1;
        } else {
            txtPlaca.requestFocusInWindow();
            aux = 0;
        }
        JButton botonesH[] = {cmdGuardar, cmdCancelar, cmdEliminar};
        JButton botonesD[] = {cmdBuscar};

        Helper.habilitarBotones(botonesH);
        Helper.deshabilitarBotones(botonesD);
    }//GEN-LAST:event_cmdBuscarActionPerformed

